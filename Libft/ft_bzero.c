#include<string.h>

void	ft_bzero(void *s, size_t n)
{
	char	*index;
	int		c;

	c = 0;
	index = s;
	if (n == 0)
		return ;
	while (n)
	{
		*index = c;
		index++;
		n--;
	}
	return ;
}
